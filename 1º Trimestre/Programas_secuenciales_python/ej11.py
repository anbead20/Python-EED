# Programa para calcular la distancia entre dos números

# Solicitar dos números al usuario
number1 = float(input("Introduce el primer número: "))
number2 = float(input("Introduce el segundo número: "))

# Calcular la distancia (valor absoluto de la diferencia)
distance = abs(number1 - number2)

# Mostrar el resultado
print(f"La distancia entre los dos números es: {distance}")

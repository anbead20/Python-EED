# Programa para calcular la media de tres números

# Solicitar tres números al usuario
number1 = float(input("Introduce el primer número: "))
number2 = float(input("Introduce el segundo número: "))
number3 = float(input("Introduce el tercer número: "))

# Calcular la media
mean = (number1 + number2 + number3) / 3

# Mostrar el resultado
print(f"La media de los tres números es: {mean}")

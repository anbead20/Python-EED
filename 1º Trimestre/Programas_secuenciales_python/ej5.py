# Programa para convertir grados Fahrenheit a grados Celsius

# Solicitar los grados Fahrenheit al usuario
fahrenheit = float(input("Introduce el valor en grados Fahrenheit: "))

# Convertir a grados Celsius
celsius = (fahrenheit - 32) * 5/9

# Mostrar el resultado
print(f"El equivalente en grados Celsius es: {celsius}")
